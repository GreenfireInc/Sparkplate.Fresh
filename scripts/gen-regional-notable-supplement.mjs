import fs from 'node:fs'

const path =
  new URL('../src/lib/cores/alliancesCore/regionalNotableUniversitiesSupplement.ts', import.meta.url).pathname

/** @typedef {[string,string,string,string,string,string]} Row */
/** @type {Record<string,[Row, Row, Row]>} */
const D = {}

function set(iso, a, b, c) {
  D[iso] = [a, b, c]
}

const uni = (...args) => args

/** @param {string[]} isos */
